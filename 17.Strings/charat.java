import java.util.*;
public class charat {
    //index deun character find karu shakto
    public static void main(String args[]){
        String str = "Pruthviraj Yewale";
        System.out.println(str.charAt(5));
        for(int i=0;i<str.length();i++){
            System.out.print(str.charAt(i)+" ");
        }
    }
}
